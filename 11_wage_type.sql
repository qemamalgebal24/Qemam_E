-- إضافة "نوع الأجر" لكل موظف: شهري (تبع الشركة) أو يومية
-- - شهري: بياخد المرتب كامل، ومفيش خصم غير الغياب أو ترك العمل
-- - يومية: بياخد بس قيمة الأيام اللي اشتغلها فعليًا (من غير جمعة/سبت/مرضي/إجازة)
-- شغّلي هذا الملف بعد كل الملفات السابقة (يحتاج index.html محدّث كمان)
-- ============================================================================

-- 1) عمود نوع الأجر في جدول الموظفين المركزي (الإعدادات ← الموظفين)
alter table public.employees
  add column if not exists wage_type text not null default 'شهري'
  check (wage_type in ('شهري', 'يومية'));

-- 2) نفس العمود في صفوف كشف كل شهر (نسخة مستقلة قابلة للتعديل لكل شهر،
--    بنفس منطق عمود "salary" المضاف في ملف 04_entry_salary.sql)
alter table public.payroll_entries
  add column if not exists wage_type text not null default 'شهري'
  check (wage_type in ('شهري', 'يومية'));

-- 3) تعبئة نوع الأجر في صفوف الشهور الحالية من نوع أجر الموظف المركزي
--    (لو الموظف لسه معرَّف "يومية" في الإعدادات، الصفوف القديمة هتتحدّث تبعًا لكده)
update public.payroll_entries pe
set wage_type = e.wage_type
from public.employees e
where pe.employee_id = e.id;

