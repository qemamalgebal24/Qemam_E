-- ============================================================================
-- إضافة "المرتب" كحقل مستقل داخل كل صف شهري (بدل الاعتماد على مرتب الموظف
-- المركزي فقط) — يسمح بتعديل المرتب مباشرة داخل جدول الرواتب لكل شهر،
-- ويطابق شكل جدول الرواتب الأصلي المطلوب.
-- شغّل هذا الملف بعد الملفات السابقة كلها.
-- ============================================================================

alter table public.payroll_entries add column if not exists salary numeric not null default 0;

-- تعبئة القيمة الحالية من مرتب الموظف المركزي كنقطة بداية لأي بيانات موجودة
update public.payroll_entries pe
set salary = e.salary
from public.employees e
where pe.employee_id = e.id and pe.salary = 0;

